"""
Leia o valor do raio de um circulo, calcule e imprima a area do circulo correspondente.
A area do circulo é a = pi * raio², considere pi = 3.141592.
"""
pi = 3.141592
r = float(input('Digite o valor do raio do circulo para obeter a sua área: '))
r2 = r ** 2
a = pi * r2
print(f'O área total do círculo é de {a}.')
