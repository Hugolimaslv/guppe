"""
Leia um valor de comprimento em metros e apresente-o convertido em jardas.
A formula de conversão é: J = M / 0.91, sendo J o comprimento em jardas e M o comprimento em metros.
"""
m = float(input('Digite o valor em metros que deseja converter para jardas: '))
j = m / 0.91

print(f'O resultado da conversão é de {j} jardas.')

