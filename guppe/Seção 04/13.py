"""
Leia uma distancia em quilometros e apresente-a convertida em milhas.
A formula de conversão é M = K / 1.61, sendo K a distancia em quilometros e M a distancia em milhas.
"""

k = float(input('Informe a distancia em quilometros que voce dejesa transformar em milhas: '))
m = k / 1.61
print(f'A transformacao de {k} quilometros tem como resultado {m} milhas')
