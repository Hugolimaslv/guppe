# Peça ao usuário para digitar três valores inteiros e imprima a soma deles

print('Digite três números inteiros e receba a soma dos mesmos')
n1 = int(input('Digíte o primeiro valor: '))
n2 = int(input('Digíte o segundo valor: '))
n3 = int(input('Digíte o terceiro valor: '))
s = n1 + n2 + n3

print(f'O resultado da soma de {n1}, {n2} e {n3} é de {s}')
