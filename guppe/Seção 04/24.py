"""
Leia um valor de área em metros quadrados m² e apresente-o convertido em acres.
A formula de conversão é: A = M * 0.000247, sendo M a area em metros quadrados e A a area em acres.
"""
m = float(input('Digite o valor da area metros quadrados que deseja converter para acres: '))
a = m * 0.000247

print(f'O resultado da conversão é de {a} hectares.')
